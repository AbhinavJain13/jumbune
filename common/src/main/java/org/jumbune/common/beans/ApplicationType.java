package org.jumbune.common.beans;

/**
 * The Enum ApplicationType.
 */
public enum ApplicationType {

	/** The read intensive. */
	READ_INTENSIVE,
	/** The write intensive. */
	WRITE_INTENSIVE,
	/** The execution intensive. */
	EXECUTION_INTENSIVE,
	/** The mix read execution intensive. */
	MIX_READ_EXECUTION_INTENSIVE,
	/** The mix write execution intensive. */
	MIX_WRITE_EXECUTION_INTENSIVE,
	/** The read write intensive. */
	READ_WRITE_INTENSIVE,
	/** The cant be classified in above. */
	CANT_BE_CLASSIFIED_IN_ABOVE;

}
